						lsp_format = "fallback",
					}
				end
			end,
			formatters_by_ft = {
				lua = { "stylua" },
				markdown = { "markdown-cli2", stop_after_first = true },
				python = { "black", stop_after_first = true },
				sql = { "sql-formatter", stop_after_first = true },
